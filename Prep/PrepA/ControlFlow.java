public class ControlFlow{
    public static void main(String[] args){
        int num = 4;
        switch (num){
            case 4:
                System.out.println("num is: " + num);
                break;
            case 5:
                System.out.println("num is: " + num);
                break;

            default:
                System.out.println("num is: " + num);
                break;
        }
    }
}